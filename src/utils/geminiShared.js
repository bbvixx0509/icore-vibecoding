/**
 * AI 급식 리포트용 프롬프트를 생성합니다.
 * @param {Array} reviews - 학생 리뷰 배열
 * @returns {string} 생성된 프롬프트 텍스트
 */
export function buildMealReportPrompt(reviews) {
  const reviewsSummary = reviews.map(r =>
    `- 닉네임: ${r.nickname}, 별점: ${r.rating}점, 추천메뉴: ${r.bestMenu}, 아쉬운메뉴: ${r.worstMenu}, 한줄평: "${r.comment}"`
  ).join('\n');

  return `고등학교 급식의 만족도를 실시간 분석하는 AI 리포터 역할을 해줘.
다음은 오늘 급식(메뉴: 등심돈까스, 얼큰김치찌개, 양배추샐러드, 상큼요구르트)에 대해 학생들이 등록한 실시간 한줄평과 별점 데이터야:

${reviewsSummary}

이 데이터를 종합적으로 분석해서 반드시 다음 JSON 형식에 정확히 맞춰서 응답을 생성해줘. JSON 코드 블럭이나 여분의 텍스트 없이 순수 JSON만 반환해야 해:
{
  "moodTitle": "오늘 급식 분위기를 재미있게 나타내는 제목 (예: '돈까스 압승 모드', '든든함은 합격, 국물은 호불호')",
  "summary": [
    "첫 번째 핵심 요약 문장 (표준어로 35~45자 내외, 예: '오늘은 등심돈까스가 가장 좋은 반응을 얻었어요.')",
    "두 번째 핵심 요약 문장 (표준어로 35~45자 내외, 예: '김치찌개는 간이 세다는 의견이 있어 조절이 권장됩니다.')"
  ],
  "memeLine": "고등학생들이 웃을 수 있는 안전한 유행어 밈 한 줄 (오직 이 항목에서만 유행어 사용 가능)",
  "improvements": ["급식을 개선하기 위한 핵심 개선 포인트 3가지 목록 (각각 2~4단어로 아주 짧고 간결한 명사형 구문으로 작성, 예: '튀김 식감 유지', '찌개 간 조절', '디저트 양 보강')"],
  "positiveKeywords": ["자주 언급된 긍정 키워드 3가지 목록"],
  "negativeKeywords": ["자주 언급된 아쉬운 키워드 2~3가지 목록"]
}

유행어/밈 사용 규칙:
- 유행어/신조어/밈은 오직 "memeLine" 항목에만 사용할 수 있으며, "moodTitle", "summary", "improvements" 등 다른 모든 항목에서는 표준어 기반의 깔끔한 한국어로 작성해야 해.
- 사용 가능 유행어: 폼 미쳤다, 급식판 캐리, 레전드, 맛도리, GOAT, 꽤 괜찮은데?, 호불호 갈림, 오늘 ○○가 다 했다, 마무리 좋다, 오히려 좋아, 야르
- 제한적 사용 가능 유행어: 킹받네 (메뉴나 학생을 공격하는 비하 표현이 아닌, '살짝 킹받을 뻔했지만'처럼 가볍고 장난스러운 셀프 디스나 유머러스한 맥락에서만 가능)
- 사용 절대 금지 단어: 밤티, 사갈, 샤갈, 개에반데, 욕설, 비속어, 외모/성별/지역/성적 관련 표현

절대 금지 사항: 비속어, 욕설, 공격적인 표현, 특정 학생을 놀리거나 비교하는 내용, 외모/성별/성적 등에 대한 언급은 절대 포함하지 마. 아주 깔끔하고 긍정적인 고등학교 대시보드 톤앤매너를 지켜줘.`;
}

/**
 * 개인별 급식 취향 타입 분석용 프롬프트를 생성합니다.
 * @param {Object} review - 학생 개별 리뷰 데이터
 * @returns {string} 생성된 프롬프트 텍스트
 */
export function buildTasteCardPrompt(review) {
  return `급식 리뷰를 남긴 학생의 개인 '급식 취향 타입'을 분석해주는 재미있는 친구 역할을 해줘.
해당 학생의 피드백 데이터는 다음과 같아:
- 닉네임: ${review.nickname}
- 별점: ${review.rating}점
- 좋았던 메뉴: ${review.bestMenu}
- 아쉬웠던 메뉴: ${review.worstMenu}
- 한줄평: "${review.comment}"

위 정보를 바탕으로 해당 학생의 성향을 귀엽고 재치있게 분석해서 반드시 다음 JSON 형식으로만 응답해줘. JSON 코드 블럭 없이 순수 JSON만 반환해야 해:
{
  "typeName": "재치 있는 취향 타입 이름 (예: '바삭파 실전형', '든든함 우선파', '매콤 밸런스파', '디저트 마무리파')",
  "oneLiner": "오늘의 급식 취향을 설명하는 공감되는 한 마디 (예: '돈까스 있으면 일단 합격 주는 타입이에요.')",
  "tags": ["이 학생의 취향을 나타내는 태그 3개 (예: '#바삭함', '#든든함', '#소스중요')"],
  "friendMatch": "이 학생과 가장 잘 어울릴 것 같은 추천 친구 타입 이름 (예: '매운맛 탐험가', '디저트 수호자', '샐러드 밸런서')"
}

유행어/밈 사용 규칙:
- 유행어/신조어/밈은 가급적 자제하고, "oneLiner"나 "tags" 항목에 포함하는 경우 아래 규칙을 따라줘.
- 사용 가능 유행어: 폼 미쳤다, 급식판 캐리, 레전드, 맛도리, GOAT, 꽤 괜찮은데?, 호불호 갈림, 오늘 ○○가 다 했다, 마무리 좋다, 오히려 좋아, 야르
- 제한적 사용 가능 유행어: 킹받네 (메뉴나 학생을 공격하는 비하 표현이 아닌, '살짝 킹받을 뻔했지만'처럼 가볍고 장난스러운 셀프 디스나 유머러스한 맥락에서만 가능)
- 사용 절대 금지 단어: 밤티, 사갈, 샤갈, 개에반데, 욕설, 비속어, 외모/성별/지역/성적 관련 표현

절대 금지 사항: 비속어, 욕설, 공격적인 묘사, 학생을 비하하는 말은 포함하면 안 돼. 따뜻하고 재치 있는 고등학교 급식 소울메이트 톤으로 작성해줘.`;
}

/**
 * 로컬 룰 베이스로 개인 취향 카드를 생성합니다.
 * @param {Object} review - 개별 리뷰 데이터
 * @returns {Object} 로컬 생성 취향 카드
 */
export function generateLocalTasteCard(review) {
  let typeName = '든든한 식사파';
  let oneLiner = '밥과 국의 든든함을 가장 중요시하는 스타일이에요.';
  let tags = ['#든든한한끼', '#한식러버', '#든든함'];
  let friendMatch = '샐러드 밸런서';

  if (review.bestMenu === '등심돈까스') {
    typeName = '바삭파 실전형';
    oneLiner = '돈까스 있으면 일단 급식실 합격점 주는 타입이에요.';
    tags = ['#바삭바삭', '#튀김러버', '#맛도리'];
    friendMatch = '디저트 수호자';
  } else if (review.bestMenu === '상큼요구르트') {
    typeName = '디저트 마무리파';
    oneLiner = '디저트가 맛있어야 완벽한 급식의 마무리가 되는 편이에요.';
    tags = ['#달달구리', '#디저트최고', '#요구르트'];
    friendMatch = '바삭파 실전형';
  } else if (review.bestMenu === '얼큰김치찌개') {
    typeName = '매콤 국물파';
    oneLiner = '얼큰한 찌개 국물로 스트레스를 푸는 스타일이에요.';
    tags = ['#얼큰찌개', '#국물이끝내줘요', '#매운맛'];
    friendMatch = '샐러드 밸런서';
  } else if (review.bestMenu === '양배추샐러드') {
    typeName = '식단 조율사';
    oneLiner = '신선하고 아삭한 샐러드로 급식 식단을 균형있게 관리해요.';
    tags = ['#신선함', '#아삭아삭', '#샐러드러버'];
    friendMatch = '매콤 국물파';
  }

  if (review.rating === 5) {
    tags.push('#완식성공');
  } else if (review.rating <= 2) {
    tags.push('#솔직한피드백');
  }

  return {
    typeName,
    oneLiner,
    tags: tags.slice(0, 3),
    friendMatch,
    isLocalTaste: true
  };
}
